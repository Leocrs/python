# len = é número de elementos de una lista
# sum = somar todos os elementos de una lista
# round = redondear os elementos de uma lista
# abs = retornar o valor absoluto de um número

lista = [1, 2, 3, 4, 5 -5]  # todas de 15 numeros (elementos)

print(len(lista))   
print(sum(lista))
print(round(sum(lista)))
print(abs(sum(lista)))
print(abs(-5))