
# Precissamos entender os loop for para usar os ranges e conecer o range para trabalhar melhor com loops for.
# ranges são utilizados para gerar seguencias numericas não aleatorias e sim de maneiras gerais.

#####################################################################

#Forma 1 valor de parada
for num in range(4):
    print(num)

###################################################################

#Forma 2 valor inicio  de parada
for num in range(4,10):
    print(num)
###################################################################

#Forma 3, valor de inicio e valor de parada passo a passo
for num in range(5, 55, 5):
    print(num)

###################################################################

#Forma 4, valor inicio e valor de parada, passo.
for num in range(10, -1,-1):
    print(num)
    
    

