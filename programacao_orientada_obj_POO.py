

# Prorgramação Orientada a Objetos - POO 
# é uma abstração da linguagem de programação que permite a criação de objetos.
# A linguagem de programação orientada a objetos é a linguagem de programação de propósito generalizado.

# Classes - Classes são objetos que possuem características específicas e comportamentos.
# Classes são usadas para criar objetos.

# Atributos - Atributos são objetos que possuem características específicas e comportamentos.
# Atributos são usados para armazenar informações.

# Métodos - Métodos são objetos que possuem características específicas e comportamentos.
# Métodos são usados para executar ações.

# Exemplo de Classes:

class Pessoa:
    def __init__(self, nome, idade):
        self.nome = nome
        self.idade = idade
print(Pessoa.__doc__)
print(Pessoa.__name__)
print(Pessoa.__module__)
print(Pessoa.__bases__)
print(Pessoa.__dict__)


